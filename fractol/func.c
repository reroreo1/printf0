#include "fractol.h"
int func(int k, t_mlx *w)
{
	if(k == 4)
	{
		w->st1 /= 2;
		w->st2 /= 2;
	}
	if(k == 5)
	{
		w->st1 *= 2;
		w->st2 *= 2;
	}
	if (w->is_julia == 1)
		julia(w);
	else if (w->is_julia == 0)
		mandelbrot(w);
	return 0;
}
